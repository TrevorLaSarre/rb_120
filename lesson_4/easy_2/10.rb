Benefits of Object Oriented Programming in Ruby:

1. It allows the developer to think at a higher level of abstraction by creating classes that reperesent real-world objects with states and behaviors
2. It allows faster development because bits of code can easily be reused
3. It allows functionality to only be exposed to the bits of code that need it
4. It helps to manage complexity inherent to larger and more involved applications (via encapsulation)

